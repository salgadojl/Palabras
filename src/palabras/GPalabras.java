    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int resultado;

        JFileChooser s = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("JPG y PNG", "jpg", "png");

        s.setFileFilter(filtro);

        resultado = s.showOpenDialog(null);

        if (JFileChooser.APPROVE_OPTION == resultado) {
            ruta = s.getSelectedFile().toString();
            jResp.setText(ruta);
            File fichero = s.getSelectedFile();

            try {

                ImageIcon icon = new ImageIcon(fichero.toString());

                Icon icono = new ImageIcon(icon.getImage().getScaledInstance(jLabel7.getWidth(), jLabel7.getHeight(), Image.SCALE_DEFAULT));

                jLabel7.setIcon(icono);

            } catch (Exception ex) {

                JOptionPane.showMessageDialog(null, "Error abriendo la Imagen " + ex);

            }

        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

